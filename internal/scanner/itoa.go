package scanner

// itoa is a minimal int-to-string helper to avoid importing "strconv" or "fmt"
// solely for error message formatting.
func itoa(n int) string {
	if n == 0 {
		return "0"
	}
	buf := make([]byte, 0, 10)
	for n > 0 {
		buf = append([]byte{byte('0' + n%10)}, buf...)
		n /= 10
	}
	return string(buf)
}
